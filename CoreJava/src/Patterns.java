
public class Patterns {

	public static void main(String[] args) {
		int i,j;
		for(i=0;i<=5;i++)
		{
			for(j=0;j<=i;j++)
			{
				System.out.print(j);
			}
			
			System.out.println();
		}
		
		for(i=4;i>=1;i--)
		{
			for(j=0;j<=i;j++)
			{
				System.out.print(j);
			}
			
			System.out.println();
		}

	}

}
